package com.dileep.ecommerce.Model;

public class Seller {

    private String sid, pid, name, phone, city, producttype, price, image, date, time, status;
    private double latitude, longitude;

    public Seller() {
    }

    public Seller(String sid, String pid, String name, String phone, String city, String producttype, String price, String image, String date, String time, String status, double latitude, double longitude) {
        this.sid = sid;
        this.pid = pid;
        this.name = name;
        this.phone = phone;
        this.city = city;
        this.producttype = producttype;
        this.price = price;
        this.image = image;
        this.date = date;
        this.time = time;
        this.status = status;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getProducttype() {
        return producttype;
    }

    public void setProducttype(String producttype) {
        this.producttype = producttype;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }
}

